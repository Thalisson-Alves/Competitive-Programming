void solve()
{
    int s, a, f;
    cin >> s >> a >> f;
    vector<int> xs(f), ys(f);
    for (auto i = 0; i < f; i++) cin >> xs[i]>> ys[i];
    sort(all(xs));
    sort(all(ys));

    cout << "(Street: " << xs[(f-1)/2] << ", Avenue: " << ys[(f-1)/2] << ")\n";
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int t;
    cin >> t;
    while (t--)
        solve();

    return 0;
}


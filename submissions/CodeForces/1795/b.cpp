void solve()
{
  int n, k;
  cin >> n >> k;
  bool s = false, e = false;
  for (int x, y; n-- and cin >> x >> y; s = (s or x == k), e = (e or y == k));
  cout << (s and e ? "YES" : "NO") << '\n';
}

int main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(nullptr);
  cout.tie(nullptr);

  int t = 1;
  cin >> t;
  for (auto i = 1; i <= t; i++)
    solve();

  return 0;
}

